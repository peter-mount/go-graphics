package text

import (
	"github.com/peter-mount/go-graphics"
	"image"
	"image/color"
)

type monoFont []byte

func (m monoFont) DrawRune(img graph.Image, p image.Point, r rune) image.Point {
	offset := int(8 * (r - ' '))
	if offset < 0 || (offset+8) >= len(m) {
		offset = len(m) - 8
	}

	for i := 0; i < 8; i++ {
		b := m[offset+i]
		for j := 0; j < 8; j++ {
			if (b & (1 << (7 - j))) != 0 {
				img.Set(p.X+j, p.Y+i, color.White)
			}
		}
	}

	return image.Point{X: p.X + 8, Y: p.Y}
}

func (m monoFont) Size(_ rune) (int, int) {
	return 8, 8
}

